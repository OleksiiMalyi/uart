  MX_TIM7_Init();
  /* USER CODE BEGIN 2 */
	const uint16_t _size = 0x1U;
	
	uint8_t *my_data;
	uint8_t *my_data_from_spi;
	unsigned char _out[8] = {" TIMEOUT"};
	uint8_t *_time_out = (uint8_t*) _out;
	
	uint8_t my_message = (uint8_t) 'J';
	uint8_t my_message_move = 0x55;
	
	my_data = &my_message;
	my_data_from_spi = &my_message_move;
